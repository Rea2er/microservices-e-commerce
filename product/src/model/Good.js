const mongoose = require('mongoose');

const goodSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  userId: {
    type: String,
    required: true,
  },
  orderId: {
    type: String,
  },
});

module.exports = mongoose.model('Good', goodSchema);
